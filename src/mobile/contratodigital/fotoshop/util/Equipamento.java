package mobile.contratodigital.fotoshop.util;

public class Equipamento {

	private int imagem;
	private String nome;
	
	public Equipamento(int imagem, String nome) {
		super();
		this.imagem = imagem;
		this.nome = nome;
	}

	public int getImagem() {
		return imagem;
	}
		
	public String getNome() {
		return nome;
	}

}
